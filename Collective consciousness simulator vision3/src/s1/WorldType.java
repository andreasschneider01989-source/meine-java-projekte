package s1;

/**
 * Enumeration representing the different world types in the simulation.
 */
public enum WorldType {
    EARTH,              // Our Earth: Internet
    DOME_WORLD,         // Protected/encircled by domes
    ALTERNATIVE_PHYSICS,// Unified fundamental forces through gravity
    GOD_PLANT,          // A connected plant containing numerous unbirthed fetuses
    ORACLE_WORLD,       // Headquarters of the "dark angel" tasked with neutralizing you
    BILLIARD_RATS,      // A world where, for instance, no cat may be incarnated
    BILLION_PEOPLE,     // A world where a growing resistance makes safe incarnation increasingly difficult
    ASTEROID_BELT,      // Life on an asteroid belt with flying mountains
    DOGSCAPE,           // Post-story Dogscape: Humans are fused with Dogscape in a sleep-like state, and the ecosystem is collapsing.
    ZOMBIE_WORLD,       // An unstable old world with incarnated carnivores
    ICE_WORLDS          // Planets where evolution never left the ocean beneath a thick ice sheet
}
